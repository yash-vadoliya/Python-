# 16. Create a list having names of months. i). Check whether December is in list or not, ii). Query the list using ‘not in’.


months = ['January', 'February', 'March', 'April', 'June', 'July', 'August', 'September', 'October', 'November', 'December']

dec = "December" in months
print("Is December in the list ",dec)


notin = "May" in months
print("Is not in the list ",notin)  


